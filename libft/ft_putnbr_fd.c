#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n == -2147483648 && write (fd, "-2147483648", 11) + 2)
		return ;
	if (n < 0 && write (fd, "-", 1) + 2)
		n *= -1;
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	c = '0' + (n % 10);
	write (fd, &c, 1);
}
